#include "Math.h";
#include <cmath>

int Sum(int a, int b) 
{
	return a + b;
}

int Subtract(int a, int b)
{
	return a - b;
}

int Multiply(int a, int b)
{
	return a * b;
}

float Divide(int a, int b)
{
	return a / b;
}


int Expo(int a, int b)
{
	return pow(a, b);
}

float SqrRoot(int a)
{
	return sqrt(a);
}
